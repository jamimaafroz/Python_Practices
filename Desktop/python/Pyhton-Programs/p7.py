# Math operations
import math

x= 9

#print(math.pi)
#print(math.sqrt(x))
print(math.pow(x, 2))  # x raised to the power of 2
print(math.ceil(3.2))  # Round up to the nearest integer
print(math.floor(3.8))  # Round down to the nearest integer
print(math.factorial(5))  # Factorial of 5  
print(math.gcd(12, 15))  # Greatest common divisor of 12 and 15
print(math.sin(math.pi / 2))  # Sine of 90 degrees (pi/2 radians)
print(math.cos(math.pi))  # Cosine of 180 degrees (pi radians)
print(math.tan(math.pi / 4))  # Tangent of 45 degrees (pi/4 radians)

result= math.sqrt(x)  # Square root of x
print(f"The square root of {x} is {result}")